/* ── i18n Engine ── */
function getLang() {
  return localStorage.getItem('bo_lang') || 'ru';
}

function setLang(lang) {
  localStorage.setItem('bo_lang', lang);
  document.documentElement.setAttribute('data-lang', lang);
  applyTranslations();
  updateLangButtons();
}

function t(key) {
  const lang = getLang();
  const entry = TRANSLATIONS[key];
  if (!entry) return key;
  return entry[lang] || entry['ru'] || key;
}

function applyTranslations() {
  const lang = getLang();
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const entry = TRANSLATIONS[key];
    if (entry) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = entry[lang] || entry['ru'];
      } else {
        el.textContent = entry[lang] || entry['ru'];
      }
    }
  });
}

function updateLangButtons() {
  const lang = getLang();
  document.querySelectorAll('.nav__lang-btn').forEach(btn => {
    btn.classList.toggle('nav__lang-btn--active', btn.dataset.lang === lang);
  });
}

/* Initialize on load */
document.addEventListener('DOMContentLoaded', () => {
  const lang = getLang();
  document.documentElement.setAttribute('data-lang', lang);
  applyTranslations();
  updateLangButtons();
});
